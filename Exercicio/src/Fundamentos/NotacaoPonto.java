package Fundamentos;

public class NotacaoPonto {

	
	public static void main(String[] args) {
		
		String s = "Bom dia X"; // String Armazena caracterios em java dentro de um variavel
		
		s=s.replace("X", "Senhora"); // altera uma palavra para outra
		s=s.toUpperCase(); // A função deixa tudo em letra maiuscula 
		s=s.concat("!!!");// serve para juntar uma string em outra
		
		System.out.println(s);
		
		
	}
	
}


